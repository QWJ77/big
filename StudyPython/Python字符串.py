# 由字符串组成的有序集合
# 非原始字符串
# 使用" ",' ',''' ''',""" """括起来
str='a\na'
print(str,type(str))
# 打印我是"漆文君"
str1="我是\"漆文君\""
print(str1)
name="s"\
    "z"
# \续行符
print(name)
# 我是\n sz
print("我是\\n sz")

# r原始字符串,不需要转义，原始展示输入内容
print(r"我是\n sz")
# 可以用括号实现换行内容写在同一行！！！
name1=("woshi"
      "sz")
print(name1)
# ''' ''''可支持跨行输出！！！
str2='''woshi
sz
123'''
print(str2)
